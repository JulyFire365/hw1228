export function createUniqueString() {
  const timestamp = +new Date() + ''
  const randomNum = parseInt((1 + Math.random()) * 65536) + ''
  return (+(randomNum + timestamp)).toString(32)
}

/**
 * Check if an element has a class
 * @param {HTMLElement} elm
 * @param {string} cls
 * @returns {boolean}
 */
export function hasClass(ele, cls) {
  return !!ele.className.match(new RegExp('(\\s|^)' + cls + '(\\s|$)'))
}

/**
 * Add class to element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function addClass(ele, cls) {
  if (!hasClass(ele, cls)) ele.className += ' ' + cls
}

/**
 * Remove class from element
 * @param {HTMLElement} elm
 * @param {string} cls
 */
export function removeClass(ele, cls) {
  if (hasClass(ele, cls)) {
    const reg = new RegExp('(\\s|^)' + cls + '(\\s|$)')
    ele.className = ele.className.replace(reg, ' ')
  }
}

// 替换邮箱字符
export function regEmail(email) {
  if (String(email).indexOf('@') > 0) {
    const str = email.split('@')
    let _s = ''
    if (str[0].length > 3) {
      for (var i = 0; i < str[0].length - 3; i++) {
        _s += '*'
      }
    }
    var new_email = str[0].substr(0, 3) + _s + '@' + str[1]
  }
  return new_email
}

// 替换手机字符
export function regMobile(mobile) {
  if (mobile.length > 7) {
    var new_mobile = mobile.substr(0, 3) + '****' + mobile.substr(7)
  }
  return new_mobile
}

// 下载文件
export function downloadFile(obj, name, suffix) {
  const url = window.URL.createObjectURL(new Blob([obj]))
  const link = document.createElement('a')
  link.style.display = 'none'
  link.href = url
  link.target = '_blank'
  const fileName = parseTime(new Date()) + '-' + name + '.' + suffix
  link.setAttribute('download', fileName)
  document.body.appendChild(link)
  link.click()
  document.body.removeChild(link)
}

// 后台返回的 验证错误信息 回显到对应表单；指定错误码为1001
export function backValidatorErr(err, form) {
  if (err.errno === 1001) {
    const obj = err.result
    for (const key in obj) {
      const field = form.fields.find(item => item.prop === key)
      field.validateState = 'error'
      field.validateMessage = obj[key].join()
    }
  }
}

// 将类似部门的列表数据转成 树状结构，arr数据列表， id子级id标识，parent父级id标识，返回树结构
export function arrToTree(arr, id = 'id', parent = 'parent', sort_id) {
  const arr2 = JSON.parse(JSON.stringify(arr))
  const tmp = {}
  let back = []
  function sort(a, b) {
    if (typeof a[id] === 'string' && !sort_id) {
      // 按某种排序标准进行比较, a 小于 b
      if (a[id] < b[id]) {
        return -1
      }
      if (a[id] > b[id]) {
        return 1
      }
      // a must be equal to b
      return 0
    } else if (sort_id) {
      return a[sort_id] - b[sort_id]
    } else {
      return a[id] - b[id]
    }
  }
  arr2.sort((a, b) => {
    if (typeof a[parent] === 'string') {
      // 按某种排序标准进行比较, a 小于 b
      if (a[parent] < b[parent] && a[parent].indexOf('顶') == 0) {
        return -1
      }
      if (a[parent] > b[parent] && a[parent].indexOf('顶') == -1) {
        return 1
      }
      // a must be equal to b
      return 0
    } else {
      return a[parent] - b[parent]
    }
  })

  // 从后向前遍历
  for (let i = arr2.length - 1; i >= 0; i--) {
    var nowPid = arr2[i][parent]
    var nowId = arr2[i][id]
    // 建立当前节点的父节点的children 数组
    if (tmp[nowPid]) {
      tmp[nowPid].push(arr2[i])
    } else {
      tmp[nowPid] = [arr2[i]]
    }
    // 将children 放入合适的位置
    if (tmp[nowId]) {
      arr2[i].children = tmp[nowId].sort(sort)
      delete tmp[nowId]
    }
  }
  for (const k in tmp) {
    back = back.concat(tmp[k].sort(sort))
  }
  return back
}
